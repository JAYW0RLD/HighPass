-- Request Logs (Existing)
CREATE TABLE IF NOT EXISTS requests (
    id bigint generated by default as identity primary key,
    agent_id text,
    timestamp timestamptz default now(),
    status integer,
    amount text,
    tx_hash text,
    endpoint text,
    error text,
    credit_grade text,
    CONSTRAINT unique_tx_hash UNIQUE (tx_hash) -- Prevent replay attacks
);

-- Create index for faster lookups
CREATE INDEX IF NOT EXISTS idx_requests_tx_hash ON requests(tx_hash) WHERE tx_hash IS NOT NULL;

-- Realtime for Requests (Idempotent fix)
DO $$
BEGIN
    IF EXISTS (SELECT 1 FROM pg_publication WHERE pubname = 'supabase_realtime') THEN
        IF NOT EXISTS (
            SELECT 1 FROM pg_publication_tables 
            WHERE pubname = 'supabase_realtime' 
            AND schemaname = 'public' 
            AND tablename = 'requests'
        ) THEN
            ALTER PUBLICATION supabase_realtime ADD TABLE requests;
        END IF;
    END IF;
END $$;

